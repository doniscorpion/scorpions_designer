function setupFeedbackForm(formId, listId) {
  const form = document.getElementById(formId);
  const list = document.getElementById(listId);
  if (!form || !list) return;
  const STORAGE_KEY = 'scorpions-feedback-demo-multipage';

  function getFeedbacks() {
    try {
      return JSON.parse(localStorage.getItem(STORAGE_KEY)) || [];
    } catch {
      return [];
    }
  }

  function saveFeedbacks(data) {
    localStorage.setItem(STORAGE_KEY, JSON.stringify(data));
  }

  function drawFeedbacks() {
    const items = getFeedbacks();
    if (!items.length) {
      list.innerHTML = '<div class="notice">Nenhum feedback salvo neste navegador ainda.</div>';
      return;
    }
    list.innerHTML = items.map(item => `
      <div class="notice">
        <strong>${item.nome}</strong>
        <p style="margin:8px 0 0;">${item.mensagem}</p>
      </div>
    `).join('');
  }

  form.addEventListener('submit', (event) => {
    event.preventDefault();
    const formData = new FormData(form);
    const nome = (formData.get('nome') || '').toString().trim();
    const mensagem = (formData.get('mensagem') || '').toString().trim();
    if (!nome || !mensagem) return;
    const items = getFeedbacks();
    items.unshift({nome, mensagem});
    saveFeedbacks(items.slice(0, 10));
    form.reset();
    drawFeedbacks();
    alert('Feedback salvo localmente neste navegador.');
  });

  drawFeedbacks();
}

function makePortfolioCard(item) {
  const media = item.type === 'youtube'
    ? `<iframe src="${item.src}" title="${item.title}" loading="lazy" allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture; web-share" allowfullscreen></iframe>`
    : `<img src="${item.src}" alt="${item.title}" loading="lazy">`;
  const badge = item.type === 'youtube' ? 'Vídeo' : (item.type === 'placeholder' ? 'Espaço' : 'Imagem');
  const extraClass = item.type === 'placeholder' ? ' placeholder-card' : '';
  return `
    <article class="glass portfolio-card${extraClass}">
      <div class="portfolio-media-wrap">
        <div class="portfolio-media">${media}</div>
        <span class="portfolio-badge">${badge}</span>
      </div>
      <div class="portfolio-content">
        <p class="small muted">${item.category}</p>
        <h3 style="margin:6px 0 0;font-size:1.08rem;font-weight:800;">${item.title}</h3>
      </div>
    </article>`;
}

function renderPortfolio(targetId, filter = 'all') {
  const el = document.getElementById(targetId);
  if (!el) return;
  const visible = portfolioItems.filter(item => {
    if (filter === 'all') return true;
    if (filter === 'video') return item.type === 'youtube';
    if (filter === 'image') return item.type !== 'youtube';
    return item.category === filter;
  });
  el.innerHTML = visible.map(makePortfolioCard).join('');
}

function activatePortfolioFilters(gridId, controlsId) {
  const controls = document.getElementById(controlsId);
  if (!controls) return;
  controls.querySelectorAll('[data-filter]').forEach(button => {
    button.addEventListener('click', () => {
      controls.querySelectorAll('[data-filter]').forEach(btn => btn.classList.remove('active'));
      button.classList.add('active');
      renderPortfolio(gridId, button.dataset.filter);
    });
  });
}

